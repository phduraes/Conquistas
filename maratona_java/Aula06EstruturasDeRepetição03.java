package maratona_java;

/**
 * 29 - Estruturas de Repetição pt 03 - Break
 * Imprima os primeiros numero 25 numeros de um dado valor.
 * Por exemplo 50
 */
public class Aula06EstruturasDeRepetição03 {

    public static void main(String[] args) {
        int valorMax = 50;
        for (int i = 0; i <= valorMax; i++) {

            if (i <= 25) {
                System.out.printf(String.valueOf(i));
    }
}


  }

}
